% File name:    prepareJoint
% -------------------------------------------------------------------------
% Subject:      Prepare Joint structure
% -------------------------------------------------------------------------
% Inputs:       - Grf (structure)
%               - side (char)
% Outputs:      - Joint (structure)
% -------------------------------------------------------------------------
% Author: F. Moissenet, C. Schreiber, A. Naaim
% Date of creation: 26/03/2014
% Version: 1
% -------------------------------------------------------------------------
% Updates: - 23/01/2015: Manage side (transform left to right)
% =========================================================================

function Joint = prepareJoint(Grf,side)

Joint(1).F = -Grf.F;
Joint(1).M = -Grf.M;

% =========================================================================
% Transform left limb data into right limb data
% =========================================================================
if strcmp(side,'Left')
    Joint(1).F(3,:,:) = -Joint(1).F(3,:,:);
    Joint(1).M(3,:,:) = -Joint(1).M(3,:,:);
end